/*
This class deals with vectors, which is a quantity that represents magnitude and direction.
*/

public class Vector {
	private double vectorX;
	private double vectorY;
	
	public Vector() {
		this.vectorX = 0;
		this.vectorY = 0;
	}
	
	public Vector(double x, double y) {
		this.vectorX = x;
		this.vectorY = y;
	}
	
	
}
